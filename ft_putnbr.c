#include "libft.h"

void ft_putnbr(int n)
{
    long k;

    k = n;
    if (k > 2147483647 || k < -2147483648)
        return;
    if (k < 0)
    {
        k = k * -1;
        ft_putchar('-');
    }
    if (k >= 10)
    {
        ft_putnbr(k / 10);
        k = k % 10;
    }
    if (k < 10)
    {
        ft_putchar(k + '0');
    }
}

int main(void)
{
    ft_putnbr(-2147483648);
    return (0);
}
