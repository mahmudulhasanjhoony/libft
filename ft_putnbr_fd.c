#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	long	k;

	k = n;
	if (k > 2147483647 || k < -2147483648)
		return ;
	if (k < 0)
	{
		k = k * -1;
		ft_putchar_fd('-', fd);
	}
	if (k >= 10)
	{
		ft_putnbr_fd(k / 10, fd);
		k = k % 10;
	}
	if (k < 10)
	{
		ft_putchar_fd((k + '0'), fd);
	}
}
