#include "libft.h"

void ft_memdel(void **ap)
{
    if (ap && *ap)
    {
        free(*ap);
        *ap = NULL;
    }
}

int main(void)
{
    int i, *p;
    p = &i;
    ft_memdel(*p);
    return (0);
}